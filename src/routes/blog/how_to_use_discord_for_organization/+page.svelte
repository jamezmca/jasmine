<h2 class="font-semibold text-2xl sm:text-3xl md:text-4xl">
    Introducing Jasmine
</h2>
<p>
    Jasmine is a Disord bot that can be engaged via the Try Jasmine Discord
    channel. Her purpose is to keep track of all of your organization
    commitments and requirements and manage them in a way that is incredibly
    easy for you to stay on top of an evergrowing task list. <br /><br />Jasmine
    understands a very easy-to-use command set that allows her to perform
    create, read, update & delete style operations on all of your tasks and
    lists, and best of all, she's available on device on which you already have
    the Discord app. So there's no more app downloads, no extra platform or
    diary to manage, and she's always there ready to help you know what's up
    next!
</p>

<h2 class="font-semibold text-2xl sm:text-3xl md:text-4xl">
    How does Jasmine work?
</h2>

<p>
    It takes less than 60 seconds to get started with Jasmine.<br /><br />First,
    you'll want to join the
    <a class="bg-emerald-950 text-emerald-400" target="_blank" href=""
        >Try Jasmine Discord channel</a
    >
    where you can engage Jasmine, in addition to receiving any type of support
    or assistance as you do so. Typing
    <span
        class=" text-amber-400 p-0.5 px-1 bg-black border border-solid border-amber-800 rounded-md"
        >/start</span
    >
    in the general channel will send Jasmine the intrustion to personally
    message you, and begin your organizational conversations; it is within your
    private messages with Jasmine that she will assist you. <br /><br />Once you
    have a private message window open with Jasmine, this is where the magic
    happens. <br /><br /> Jasmine understands 4 primary commands with which you
    can achieve just about any organizational feat; these commands are used in
    sentences with a define structure to manage your tasks. It should also be
    noted that all tasks are categorized into a list -> so Jasmine is
    effectively managing all of your lists with each of their respective list
    items. The keywords denominated by the primary create, read, update and
    delete functionalities are<br /><br />
    <span
        class=" text-amber-400 p-0.5 px-1 bg-black border border-solid border-amber-800 rounded-md"
        >summary, add, update, delete</span
    ><br /><br /><b>Summary</b> is used to display either all of your tasks
    inside of their respective lists, or alternatively it can show you the tasks
    specific to a singular list, <b>add</b> is used to add one or more tasks to
    a particular list, <b>update</b> is used to ammend or update an alreay
    existing list item within a particular list and finally, <b>delete</b>is
    used to delete one or more tasks from a list.<br /><br />For more
    information and examples on how the command language works, type
    <span
        class=" text-amber-400 p-0.5 px-1 bg-black border border-solid border-amber-800 rounded-md"
        >help</span
    > within your private chat window with Jasmine.
</p>

